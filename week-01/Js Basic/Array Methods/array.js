
let fruits_container = ["Apple","Bananan","Penapple","Mango"];
console.log(fruits_container)

// pop into the last
fruits_container.pop();
console.log(fruits_container);

// pop into the front 
fruits_container.unshift();
console.log(fruits_container);

// push new element 
fruits_container.push("newFruits");
console.log(fruits_container)

// push into the front
fruits_container.shift();
console.log(fruits_container)